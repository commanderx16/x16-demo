*=$0801               ;START ADDRESS IS $0801
VERA_INC=$9F22
VERA_HI=$9F21
VERA_LO=$9F20
VERA_RW=$9F23

BASIC:	!BYTE $0B,$08,$01,$00,$9E,$32,$30,$36,$31,$00,$00,$00
	;Adds BASIC line:  1 SYS 2061
INIT1:
	JSR	SWBANK2

	JSR	SET_MODE

INIT2:
	JSR	CLEAR_SCREEN
	LDA	#00	;SET INITIAL VALUES
	STA	XLOC
	STA	YLOC
	LDA	#01
	STA	PCOL
	LDA	#40
	STA	SCR_SIZE_X
	LDA	#30
	STA SCR_SIZE_Y
	JSR	MAIN_CURSOR_PLOT
	JSR	HELP_SCREEN
	
MAIN_DRAW_SCREEN:

MAIN_DRAW_KEYWAIT:
	JSR	$FFE4
	CMP	#$00
	BEQ	MAIN_DRAW_KEYWAIT
	CMP	#$91	;CURSOR UP
	BNE	MDK01
	JSR	MAIN_KEY_UP
	JMP	MAIN_DRAW_KEYWAIT
MDK01:
	CMP	#$11	;CURSOR DOWN
	BNE	MDK02
	JSR	MAIN_KEY_DOWN
	JMP	MAIN_DRAW_KEYWAIT
MDK02:	
	CMP	#$9D	;CURSOR LEFT
	BNE MDK03
	JSR	MAIN_KEY_LEFT
	JMP	MAIN_DRAW_KEYWAIT
MDK03:
	CMP	#$1D	;CURSOR RIGHT
	BNE MDK04
	JSR	MAIN_KEY_RIGHT
	JMP	MAIN_DRAW_KEYWAIT
MDK04:
	CMP	#$0D	;RETURN
	BNE MDK05
	JSR	CHARACTER_SELECT_SCREEN
	JMP	MAIN_DRAW_KEYWAIT
MDK05:
	CMP	#32		;SPACE BAR
	BNE	MDK06
	JSR	MAIN_CURSOR_UNPLOT
	LDA	SELCOL
	STA PCOL
	JSR	PLOTCHAR
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK06:
	CMP	#$48	;H KEY
	BNE	MDK07
	JSR	HELP_SCREEN
	JMP	MAIN_DRAW_KEYWAIT
MDK07:
	CMP	#137	;F2-KEY (PIXEL DRAW MODE)
	BNE	MDK08
	JMP	PIXEL_DRAW_SCREEN
MDK08:
	CMP	#$47	;G-key	
	BNE	MDK09:
	JSR	MAIN_CURSOR_UNPLOT
	JSR	GETCHAR
	LDA	PCOL
	STA	SELCOL
	JSR MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT	
MDK09:
	CMP	#$1F	;BLUE
	BNE	MDK10
	LDA	#06		;COLOR BLUE
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK10:
	CMP	#$05	;WHITE
	BNE	MDK11
	LDA	#01		;COLOR WHITE
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK11:
	CMP	#$1C	;RED
	BNE	MDK12
	LDA	#02		;COLOR RED
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK12:
	CMP	#138	;F4-KEY
	BNE	MDK13
	JSR	MAIN_CURSOR_UNPLOT
	JSR	COLOR_SCREEN
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK13:
	CMP	#134	;F3-KEY (TYPING MODE)
	BNE	MDK14
	JMP	TYPE_MODE
MDK14:
	CMP	#$14	;BACKSPACE
	BNE	MDK15	
	JSR	MAIN_CURSOR_UNPLOT
	LDA	#32
	STA	PCHAR
	JSR	PLOTCHAR
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK15:
	CMP	#$90	;BLACK
	BNE	MDK16
	LDA	#00		;COLOR BLACK
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK16:
	CMP	#$9F	;CYAN
	BNE	MDK17
	LDA	#03		;COLOR CYAN
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK17:
	CMP	#$9C	;PURPLE
	BNE	MDK18
	LDA	#04		;COLOR PURPLE
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK18:
	CMP	#$1E	;GREEN
	BNE	MDK19
	LDA	#05		;COLOR GREEN
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK19:
	CMP	#$9E	;YELLOW
	BNE	MDK20
	LDA	#07		;COLOR YELLOW
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK20:
	CMP	#$81	;ORANGE
	BNE	MDK21
	LDA	#08		;COLOR ORANGE
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK21:
	CMP	#$95	;BROWN
	BNE	MDK22
	LDA	#09		;COLOR BROWN
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK22:
	CMP	#$96	;LIGHT RED
	BNE	MDK23
	LDA	#10		;COLOR LIGHT RED
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK23:
	CMP	#$97	;DARK GRAY
	BNE	MDK24
	LDA	#11		;COLOR DARK GRAY
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK24:
	CMP	#$98	;GRAY
	BNE	MDK25
	LDA	#12		;COLOR GRAY
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK25:
	CMP	#$99	;LIGHT GREEN
	BNE	MDK26
	LDA	#13		;COLOR LIGHT GREEN
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK26:
	CMP	#$9A	;LIGHT BLUE
	BNE	MDK27
	LDA	#14		;COLOR LIGHT BLUE
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK27:
	CMP	#$9B	;LIGHT GRAY
	BNE	MDK28
	LDA	#15		;COLOR LIGHT GRAY
	JSR	KEYS_SET_COLOR
	JMP	MAIN_DRAW_KEYWAIT
MDK28:
	CMP	#135	;F5 KEY (SETUP)
	BNE MDK29
	JSR	SETUP_MENU
	JMP	MAIN_DRAW_KEYWAIT
MDK29:	
	CMP	#$43	;C-KEY
	BNE	MDK30
	JSR	PASTE_COLOR_ONLY
	JMP	MAIN_DRAW_KEYWAIT
MDK30:
	CMP	#139	;F6 key (LOAD/SAVE)
	BNE	MDK31
	JSR	MAIN_CURSOR_UNPLOT
	JSR	LOAD_SAVE_MENU
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT	
MDK31:	
	CMP	#$93	;CLR-SCREEN
	BNE	MDK32:
	JSR	CLEAR_SCREEN
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK32:
	CMP	#$D5	;SHIFT-U
	BNE	MDK33
	JSR	MAIN_CURSOR_UNPLOT
	JSR	SHIFT_LINE_UP
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK33:	
	CMP	#$C4	;SHIFT-D
	BNE	MDK34
	JSR	MAIN_CURSOR_UNPLOT
	JSR	SHIFT_LINE_DOWN
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK34:
	CMP	#$CC	;SHIFT-L
	BNE	MDK35
	JSR	MAIN_CURSOR_UNPLOT
	JSR	SHIFT_LINE_LEFT
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK35:
	CMP	#$D2	;SHIFT-R
	BNE	MDK36
	JSR	MAIN_CURSOR_UNPLOT
	JSR	SHIFT_LINE_RIGHT
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_KEYWAIT
MDK36:
	JMP	MAIN_DRAW_KEYWAIT

SHIFT_LINE_RIGHT:
	LDA	SCR_SIZE_X
	SEC
	SBC	#1
	STA	XTEMP
	LDA	#%00010000	;INCREMENT SET TO 1
	STA	VERA_INC
	;FIRST GRAB RIGHT CHARACTER+COLOR FOR STORAGE
	LDA	YLOC
	STA	VERA_HI
	LDA	SCR_SIZE_X
	SEC
	SBC	#1
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	SHIFT_TEMP1
	LDA	VERA_RW
	STA	SHIFT_TEMP2
SHRT02:	
	;NEXT GET BYTE FROM COLUMN TO LEFT
	LDA	XTEMP
	SEC
	SBC	#1
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	CHRTEMP
	LDA	VERA_RW
	STA	COLTEMP
	;NOW STORE TO CURRENT COLUMN
	LDA	XTEMP
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	CHRTEMP
	STA	VERA_RW
	LDA	COLTEMP
	STA	VERA_RW
	;NOW MOVE TO LEFT
	DEC	XTEMP
	LDA	XTEMP
	CMP	#0
	BNE	SHRT02
	;NOW PLACE TEMP CHAR AT LEFT OF SCREEN
	LDA	0
	STA	VERA_LO
	LDA	SHIFT_TEMP1
	STA	VERA_RW
	LDA	SHIFT_TEMP2
	STA	VERA_RW
	RTS

SHIFT_LINE_LEFT:
	LDA	#0
	STA	XTEMP
	LDA	#%00010000	;INCREMENT SET TO 1
	STA	VERA_INC
	;FIRST GRAB LEFT CHARACTER+COLOR FOR STORAGE
	LDA	YLOC
	STA	VERA_HI
	LDA	#0
	STA	VERA_LO
	LDA	VERA_RW
	STA	SHIFT_TEMP1
	LDA	VERA_RW
	STA	SHIFT_TEMP2
SHLF02:	
	;NEXT GET BYTE FROM COLUMN TO RIGHT
	LDA	XTEMP
	CLC
	ADC	#1
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	CHRTEMP
	LDA	VERA_RW
	STA	COLTEMP
	;NOW STORE TO CURRENT COLUMN
	LDA	XTEMP
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	CHRTEMP
	STA	VERA_RW
	LDA	COLTEMP
	STA	VERA_RW
	;NOW MOVE TO RIGHT
	INC	XTEMP
	LDA	SCR_SIZE_X
	SEC
	SBC	#1
	CMP	XTEMP
	BNE	SHLF02
	;NOW PLACE TEMP CHAR AT RIGHT OF SCREEN
	LDA	SCR_SIZE_X
	SEC
	SBC	#1
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	SHIFT_TEMP1
	STA	VERA_RW
	LDA	SHIFT_TEMP2
	STA	VERA_RW
	RTS

SHIFT_LINE_UP:
	LDA	#0
	STA	YTEMP
	LDA	#%00010000	;INCREMENT SET TO 1
	STA	VERA_INC
	;FIRST GRAB TOP CHARACTER+COLOR FOR STORAGE
	LDA	#0
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	SHIFT_TEMP1
	LDA	VERA_RW
	STA	SHIFT_TEMP2
SHUP02:	
	;NEXT GET BYTE FROM ROW BELOW
	LDA	YTEMP
	CLC
	ADC	#1
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	CHRTEMP
	LDA	VERA_RW
	STA	COLTEMP
	;NOW STORE TO CURRENT ROW
	LDA	YTEMP
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	CHRTEMP
	STA	VERA_RW
	LDA	COLTEMP
	STA	VERA_RW
	;NOW MOVE DOWN
	INC	YTEMP
	LDA	SCR_SIZE_Y
	SEC
	SBC	#1
	CMP	YTEMP
	BNE	SHUP02
	;NOW PLACE TEMP CHAR AT BOTTOM OF SCREEN
	LDA	SCR_SIZE_Y
	SEC
	SBC	#1
	STA	VERA_HI	
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	SHIFT_TEMP1
	STA	VERA_RW
	LDA	SHIFT_TEMP2
	STA	VERA_RW
	RTS

SHIFT_LINE_DOWN:
	LDA	SCR_SIZE_Y
	SEC
	SBC	#1
	STA	YTEMP
	LDA	#%00010000	;INCREMENT SET TO 1
	STA	VERA_INC
	;FIRST GRAB BOTTOM CHARACTER+COLOR FOR STORAGE
	LDA	SCR_SIZE_Y
	SEC
	SBC	#1
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	SHIFT_TEMP1
	LDA	VERA_RW
	STA	SHIFT_TEMP2
SHDN02:	
	;NEXT GET BYTE FROM ROW ABOVE
	LDA	YTEMP
	SEC
	SBC	#1
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	CHRTEMP
	LDA	VERA_RW
	STA	COLTEMP
	;NOW STORE TO CURRENT ROW
	LDA	YTEMP
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	CHRTEMP
	STA	VERA_RW
	LDA	COLTEMP
	STA	VERA_RW
	;NOW MOVE UP
	DEC	YTEMP
	LDA	YTEMP
	CMP	0
	BNE	SHDN02
	;NOW PLACE TEMP CHAR AT TOP OF SCREEN
	LDA	#0
	STA	VERA_HI	
	LDA	XLOC
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	SHIFT_TEMP1
	STA	VERA_RW
	LDA	SHIFT_TEMP2
	STA	VERA_RW
	RTS

PASTE_COLOR_ONLY:
	LDA	SELCOL
	STA	CRSR_OLD_COL
	JSR	MAIN_CURSOR_UNPLOT
	JSR	MAIN_CURSOR_PLOT
	RTS

KEYS_SET_COLOR:
	AND	#%00001111
	TAX
	LDA	SELCOL
	AND	#%11110000
	STA	SELCOL
	TXA
	ORA	SELCOL
	STA	SELCOL
	RTS

MAIN_KEY_UP:
	JSR	MAIN_CURSOR_UNPLOT
	LDA	YLOC
	CMP	#0
	BEQ	MKU1
	DEC	YLOC
MKU1:
	JSR	MAIN_CURSOR_PLOT
	RTS

MAIN_KEY_DOWN:
	JSR	MAIN_CURSOR_UNPLOT
	LDA	YLOC
	CLC
	ADC	#01
	CMP	SCR_SIZE_Y
	BEQ	MKD1
	INC	YLOC
MKD1:
	JSR	MAIN_CURSOR_PLOT
	RTS

MAIN_KEY_LEFT:
	JSR	MAIN_CURSOR_UNPLOT
	LDA	XLOC
	CMP	#0
	BEQ	MKU1
	DEC	XLOC
MKL1:
	JSR	MAIN_CURSOR_PLOT
	RTS

MAIN_KEY_RIGHT:
	JSR	MAIN_CURSOR_UNPLOT
	LDA	XLOC
	CLC
	ADC	#01
	CMP	SCR_SIZE_X
	BEQ	MKR1
	INC	XLOC
MKR1:
	JSR	MAIN_CURSOR_PLOT
	RTS

;currently the cursor plot just inverts the color, but
;I plan to make it more intersting later.

MAIN_CURSOR_PLOT:
	LDA	#00
	STA	VERA_INC	;INCREMENT SET TO ZERO
	LDA	YLOC
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	CLC
	ADC	#01
	STA	VERA_LO
	LDA	VERA_RW	;GET COLOR
	STA	CRSR_OLD_COL
	STA CHRTEMP
	ASL			;INVERT COLORS
	ASL
	ASL
	ASL
	LSR	CHRTEMP
	LSR	CHRTEMP
	LSR	CHRTEMP
	LSR	CHRTEMP
	ORA	CHRTEMP
	STA	VERA_RW
	RTS

MAIN_CURSOR_UNPLOT:
	LDA	#00
	STA	VERA_INC	;INCREMENT SET TO ZERO
	LDA	YLOC
	STA	VERA_HI
	LDA	XLOC
	ASL		;MULT BY 2
	CLC
	ADC	#01
	STA	VERA_LO
	LDA	CRSR_OLD_COL
	STA	VERA_RW
	RTS

CHARACTER_SELECT_SCREEN:
	JSR	MAIN_CURSOR_UNPLOT
	JSR	COPY_SCREEN_TO_BUFFER
	JSR	SET40
	
;This routine is a bit unique because I had to design the
;character pick screen from the C64 version of PetDraw which
;stores the data in a very different way.  So this routine
;converts it while simultaneously displays it on screen.	
	
CSS01:
	LDA	#%00010000	;INCREMENT BY 1
	STA	VERA_INC
	LDY	#00
	STY	VERA_HI
	STY	VERA_LO		;SET STARTING POSITION
	LDA	#<CHAR_SEL_SCREEN_TEXT+2
	STA	$FB
	LDA	#>CHAR_SEL_SCREEN_TEXT+2
	STA	$FC
	LDA	#<CHAR_SEL_SCREEN_COLOR+2
	STA	$FD
	LDA	#>CHAR_SEL_SCREEN_COLOR+2
	STA	$FE
	JSR	SCREENCOPY
	JSR	CHAR_SEL_CURSOR_PLOT

CHAR_SEL_KEYWAIT:
	JSR	$FFE4
	CMP	#$00
	BEQ	CHAR_SEL_KEYWAIT
	CMP	#$91	;CURSOR UP
	BNE	CSK01
	JMP	CHAR_SEL_KEY_UP
CSK01:
	CMP	#$11	;CURSOR DOWN
	BNE	CSK02
	JMP	CHAR_SEL_KEY_DOWN
CSK02:	
	CMP	#$9D	;CURSOR LEFT
	BNE CSK03
	JMP	CHAR_SEL_KEY_LEFT
CSK03:
	CMP	#$1D	;CURSOR RIGHT
	BNE CSK04
	JMP	CHAR_SEL_KEY_RIGHT
CSK04:
	CMP	#$0D	;RETURN
	BNE CSK05
	JMP	CHAR_SEL_FINISHED
CSK05:
	CMP	#$31	;1-KEY
	BNE CSK06
	JSR	SWBANK1
	JSR	CHAR_SEL_CURSOR_PLOT
	JMP	CHAR_SEL_KEYWAIT
CSK06:
	CMP	#$32	;2-KEY
	BNE CSK07
	JSR	SWBANK2
	JSR	CHAR_SEL_CURSOR_PLOT
	JMP	CHAR_SEL_KEYWAIT
CSK07:
	JMP	CHAR_SEL_KEYWAIT

CHAR_SEL_KEY_UP:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	CHAR_SEL_Y
	CMP	CHAR_SEL_MIN_Y
	BEQ	CSKU1
	DEC	CHAR_SEL_Y
CSKU1:
	JSR	CHAR_SEL_CURSOR_PLOT	
	JMP	CHAR_SEL_KEYWAIT

CHAR_SEL_KEY_DOWN:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	CHAR_SEL_Y
	CMP	CHAR_SEL_MAX_Y
	BEQ	CSKD1
	INC	CHAR_SEL_Y
CSKD1:
	JSR	CHAR_SEL_CURSOR_PLOT	
	JMP	CHAR_SEL_KEYWAIT

CHAR_SEL_KEY_LEFT:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	CHAR_SEL_X
	CMP	CHAR_SEL_MIN_X
	BEQ	CSKL1
	DEC	CHAR_SEL_X
CSKL1:
	JSR	CHAR_SEL_CURSOR_PLOT	
	JMP	CHAR_SEL_KEYWAIT

CHAR_SEL_KEY_RIGHT:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	CHAR_SEL_X
	CMP	CHAR_SEL_MAX_X
	BEQ	CSKR1
	INC	CHAR_SEL_X
CSKR1:
	JSR	CHAR_SEL_CURSOR_PLOT	
	JMP	CHAR_SEL_KEYWAIT

CHAR_SEL_CURSOR_PLOT:
	LDA	CHAR_SEL_Y
	STA	VERA_HI
	LDA	CHAR_SEL_X
	ASL		;MULT BY 2
	CLC
	ADC	#01		;ADD ONE FOR COLORSPACE
	STA	VERA_LO
	LDA	CHAR_SEL_COLOR
	STA	VERA_RW
	RTS

CHAR_SEL_CURSOR_UNPLOT:
	LDA	CHAR_SEL_Y
	STA	VERA_HI
	LDA	CHAR_SEL_X
	ASL		;MULT BY 2
	CLC
	ADC	#01		;ADD ONE FOR COLORSPACE
	STA	VERA_LO
	LDA	CHAR_SEL_NORM
	STA	VERA_RW
	RTS

SWBANK1:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	#07
	STA	CHAR_SEL_MIN_Y
	LDA	#22
	STA	CHAR_SEL_MAX_Y
	LDA	#0
	STA	CHAR_SEL_MIN_X
	LDA	#19
	STA	CHAR_SEL_MAX_X
	LDA	#97
	STA	CHAR_SEL_COLOR
	LDA	#12
	STA	CHAR_SEL_NORM
	LDA	#10
	STA	CHAR_SEL_X
	LDA	#14
	STA	CHAR_SEL_Y
	RTS
SWBANK2:
	JSR	CHAR_SEL_CURSOR_UNPLOT
	LDA	#09 
	STA	CHAR_SEL_MIN_Y
	LDA	#23
	STA	CHAR_SEL_MAX_Y
	LDA	#23
	STA	CHAR_SEL_MIN_X
	LDA	#38
	STA	CHAR_SEL_MAX_X
	LDA	#01
	STA	CHAR_SEL_COLOR
	LDA	#06
	STA	CHAR_SEL_NORM
	LDA	#30
	STA	CHAR_SEL_X
	LDA	#15
	STA	CHAR_SEL_Y
	RTS

CHAR_SEL_FINISHED:
	LDA	CHAR_SEL_Y
	STA	VERA_HI
	LDA	CHAR_SEL_X
	ASL		;MULT BY 2
	STA	VERA_LO
	LDA	VERA_RW
	STA	PCHAR
	JSR	SET_MODE
	JSR	COPY_BUFFER_TO_SCREEN
	JSR	MAIN_CURSOR_PLOT
	RTS
	
; The following routing is the Pixel-Draw screen
; which allows the user to draw using an 80x50 matrix
; of PETSCII block characters. it is accessed by
; pressing F3.

PIXEL_DRAW_SCREEN:
	JSR	MAIN_CURSOR_UNPLOT
	LDA	XLOC	;convert coordinates from regular screen to 
	ASL			;the pixel-draw matrix so that cursor shows
	STA	PX		;up around the same place.
	LDA	YLOC
	ASL	
	STA	PY
	JSR	POSTMOVE

PXGETKEYBOARD:
	JSR	PXCURSOR
	JSR	$FFE4
	CMP	#$00
	BEQ	PXGETKEYBOARD
	CMP	#138	; F4
	BNE	PXKEY01
	JSR	PREMOVE
	JSR	COLOR_SCREEN
	JSR	POSTMOVE
PXKEY01	CMP	#$85	;F1-KEY
	BNE	PXKEY02
	JSR	PREMOVE
	JSR	MAIN_CURSOR_PLOT
	JMP	MAIN_DRAW_SCREEN
PXKEY02	CMP	#$91	;CURSOR UP
	BNE	PXKEY03
	JSR	PXUP
	JMP	PXGETKEYBOARD
PXKEY03	CMP	#$11	;CURSOR DOWN
	BNE	PXKEY04
	JSR	PXDOWN
	JMP	PXGETKEYBOARD
PXKEY04	CMP	#$9D	;CURSOR LEFT
	BNE	PXKEY05
	JSR	PXLEFT
	JMP	PXGETKEYBOARD
PXKEY05	CMP	#$1D	;CURSOR RIGHT
	BNE	PXKEY06	
	JSR	PXRIGHT
	JMP	PXGETKEYBOARD
PXKEY06	CMP	#$20	;SPACE
	BNE	PXKEY07
	JSR	PIXELPLOT
	LDA	PCHAR
	STA	CHRTEMP
	LDA	SELCOL
	STA	COLTEMP
