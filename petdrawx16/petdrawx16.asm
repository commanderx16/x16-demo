	LDA	#$42	;V-SCALE
	STA	VERA_LO
	LDY	TEXT_MODE
	LDA	MODEY,Y
	STA	VERA_RW	
	LDA	#0	;BANK 0
	STA	VERA_INC
	LDA	SIZEX,Y
	STA	SCR_SIZE_X
	LDA	SIZEY,Y
	STA	SCR_SIZE_Y
	RTS

