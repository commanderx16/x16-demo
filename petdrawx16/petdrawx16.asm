	CMP	#20	;BACKSPACE
	BNE	TM20
	JSR	TYPE_BACKSPACE
	JMP	TYPE_MODE
TM20:
	JSR	PETSCII_TO_SCREENCODE
	STA	PCHAR
	JSR	MAIN_CURSOR_UNPLOT
	LDA	SELCOL
	STA PCOL
	JSR	PLOTCHAR
	LDA	SCR_SIZE_X
	SEC
	SBC	#1
	CMP	XLOC
	BEQ	TM21		
	INC	XLOC
TM21:
	JSR	MAIN_CURSOR_PLOT
	JMP	TYPE_MODE

